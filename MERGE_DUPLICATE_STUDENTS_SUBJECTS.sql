      '11349', '11360', '11395', '11427', '11432', '11441'
    )
  )
)
AND (s.institute_id, s.name) IN (
  SELECT s2.institute_id, s2.name
  FROM public.students s2
  GROUP BY s2.institute_id, s2.name
  HAVING COUNT(*) > 1
);

